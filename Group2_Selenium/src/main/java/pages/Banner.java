package pages;

public class Banner {

	public String getBannerName() {
		return bannerName;
	}
	public void setBannerName(String bannerName) {
		this.bannerName = bannerName;
	}
	public String getBannerAlias() {
		return bannerAlias;
	}
	public void setBannerAlias(String bannerAlias) {
		this.bannerAlias = bannerAlias;
	}
	public String getBannerCategory() {
		return bannerCategory;
	}
	public void setBannerCategory(String bannerCategory) {
		this.bannerCategory = bannerCategory;
	}
	public String getBannerStatus() {
		return bannerStatus;
	}
	public void setBannerStatus(String bannerStatus) {
		this.bannerStatus = bannerStatus;
	}
	public String getBannerType() {
		return bannerType;
	}
	public void setBannerType(String bannerType) {
		this.bannerType = bannerType;
	}
	public String getBannerImage() {
		return bannerImage;
	}
	public void setBannerImage(String bannerImage) {
		this.bannerImage = bannerImage;
	}
	public String getBannerWidth() {
		return bannerWidth;
	}
	public void setBannerWidth(String bannerWidth) {
		this.bannerWidth = bannerWidth;
	}
	public String getBannerHeight() {
		return bannerHeight;
	}
	public void setBannerHeight(String bannerHeight) {
		this.bannerHeight = bannerHeight;
	}
	public String getBannerClient() {
		return bannerClient;
	}
	public void setBannerClient(String bannerClient) {
		this.bannerClient = bannerClient;
	}
	private String bannerName;
	private String bannerAlias;
	private String bannerCategory;
	private String bannerStatus;
	private String bannerType;
	private String bannerImage;
	private String bannerWidth;
	private String bannerHeight;
	private String bannerClient;
}
